      ev0_rs1_data_ex <= '0;
      ev0_rs2_data_ex <= '0;
      ev0_pc_ex       <= '0;
      ev1_enable_ex   <= 1'b0;
      ev1_opcode_ex   <= '0;
      ev1_funct3_ex   <= '0;
      ev1_funct7_ex   <= '0;
      ev1_rd_ex       <= '0;
      ev1_rs1_addr_ex <= '0;
      ev1_rs2_addr_ex <= '0;
      ev1_imm_ex      <= '0;
      ev1_rs1_data_ex <= '0;
      ev1_rs2_data_ex <= '0;
      ev1_pc_ex       <= '0;
      od0_enable_ex   <= 1'b0;
      od0_opcode_ex   <= '0;
      od0_funct3_ex   <= '0;
      od0_rd_ex       <= '0;
      od0_rs1_addr_ex <= '0;
      od0_rs2_addr_ex <= '0;
      od0_imm_ex      <= '0;
      od0_rs1_data_ex <= '0;
      od0_rs2_data_ex <= '0;
      od0_pc_ex       <= '0;
      od1_enable_ex   <= 1'b0;
      od1_opcode_ex   <= '0;
      od1_funct3_ex   <= '0;
      od1_rd_ex       <= '0;
      od1_rs1_addr_ex <= '0;
      od1_rs2_addr_ex <= '0;
      od1_imm_ex      <= '0;
      od1_rs1_data_ex <= '0;
      od1_rs2_data_ex <= '0;
      od1_pc_ex       <= '0;
