      i1_reg_write_ex <= 1'b0;
      i0_pc_ex        <= '0;
      i1_pc_ex        <= '0;
      ev0_enable_ex   <= 1'b0;
      ev0_opcode_ex   <= '0;
      ev0_funct3_ex   <= '0;
      ev0_funct7_ex   <= '0;
